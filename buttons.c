void buttons_init(void)
{
	
  // Turn on the input enables for each button
  PORT->Group[BTN_0_GROUP].PINCFG[BTN_0_PIN].bit.INEN = 1;
  PORT->Group[BTN_1_GROUP].PINCFG[BTN_1_PIN].bit.INEN = 1;
  PORT->Group[BTN_ACTIVE_GROUP].PINCFG[BTN_ACTIVE_PIN].bit.INEN = 1;
  
  // Make sure the directions regs are configured as inputs
  PORT->Group[BTN_0_GROUP].DIRCLR.reg = BTN_0;
  PORT->Group[BTN_1_GROUP].DIRCLR.reg = BTN_1;
  PORT->Group[BTN_ACTIVE_GROUP].DIRCLR.reg = BTN_ACTIVE;
}

uint8_t buttons_get()
{
  uint32_t button_active;
  uint32_t button_0;
  uint32_t button_1;
  uint8_t selected_button = 0;
  
  //Get the button values
  button_active = (PORT->Group[BTN_ACTIVE_GROUP].IN.reg & BTN_ACTIVE) >> 8;
  button_0 = (PORT->Group[BTN_0_GROUP].IN.reg  & BTN_0) >> 9;
  button_1 = (PORT->Group[BTN_1_GROUP].IN.reg & BTN_1) >> 4;
  
  //If there was a botton pressed, figure out which one it was based on button 0
  // and 1
  if (button_active == 1)
  {
    if ((button_0 == 1) && (button_1 == 1))
    {
      selected_button = PB_3;
    }
    else if ((button_0 == 1) && (button_1 == 0))
    {
      selected_button = PB_1;
    }
    else if ((button_0 == 0) && (button_1 == 1))
    {
      selected_button = PB_2;
    }
    else if ((button_0 == 0) && (button_1 == 0))
    {
      selected_button = PB_0;
    }
  }
  
  return selected_button;
}

